#!/usr/bin/env python3
"""pm-ops deterministic core.

One checked-in CLI for every load-bearing operation the pm-ops skills perform,
so no decision logic lives in skill prose. Stdlib only.

Artifacts are markdown files with YAML front-matter. Front-matter is the
source of truth and the engine-neutral "normalized task" the board engines
consume. Collections are written in YAML *flow* style (`[a, b]`, `{k: v}`) so
this file can parse them without a YAML dependency; reads also accept simple
block style and use PyYAML when it happens to be installed.

Subcommands:
  init     [--repo DIR] [--template DIR]   scaffold a central PM repo (idempotent)
  new-id   [--repo DIR] [--prefix PM]      allocate the next stable id (prints e.g. PM-0042)
  read     FILE                            print the file's front-matter as JSON
  set      FILE KEY=VALUE ...              upsert front-matter keys (VALUE is JSON or a string)
  normalize FILE                           print the engine-neutral normalized task JSON
  reindex  [--repo DIR]                    regenerate index.md from every artifact

Exit codes: 0 ok · 2 usage/validation error · 3 not found · 1 unexpected error.
"""
from __future__ import annotations

import json
import os
import re
import sys
from collections import OrderedDict

# Folders that hold artifacts, in lifecycle order. Stage label = folder key.
STAGE_DIRS = OrderedDict([
    ("inbox", "inbox"),
    ("backlog", "backlog"),
    ("draft", "specs/draft"),
    ("active", "specs/active"),
    ("promoted", "specs/promoted"),
])

# Keys carried into the normalized task an engine consumes.
NORMALIZED_KEYS = [
    "id", "title", "type", "status", "size", "priority", "rigor",
    "parent", "depends_on", "spec", "impl_repo", "labels", "assignees", "board",
]

DEFAULT_CONFIG = {
    "engine": "github",
    "id_prefix": "PM",
    "github": {
        "owner": "",
        "project": 0,
        "default_repo": "",
        "repo_map": {},
        "type_field": "native",
        "fields": {
            "size": "Size",
            "priority": "Priority",
            "stage": "Status",
            "pm_id": "PM-ID",
            "spec_url": "Spec",
        },
    },
}

DEFAULT_REGISTRY = {"prefix": "PM", "next": 1}

INDEX_HEADER = "# pm-ops index\n\nMaster cross-link of every item. Regenerated by `pm.py reindex` — do not hand-edit.\n"


# --------------------------------------------------------------------------- #
# Errors
# --------------------------------------------------------------------------- #
class PmError(Exception):
    def __init__(self, msg: str, code: int = 2):
        super().__init__(msg)
        self.code = code


# --------------------------------------------------------------------------- #
# Repo discovery
# --------------------------------------------------------------------------- #
def find_repo_root(start: str) -> str:
    """Walk up from `start` looking for a `.pm-ops/` dir; fall back to `start`."""
    cur = os.path.abspath(start)
    if os.path.isfile(cur):
        cur = os.path.dirname(cur)
    while True:
        if os.path.isdir(os.path.join(cur, ".pm-ops")):
            return cur
        parent = os.path.dirname(cur)
        if parent == cur:
            return os.path.abspath(start if os.path.isdir(start) else os.path.dirname(start) or ".")
        cur = parent


# --------------------------------------------------------------------------- #
# Front-matter parse / serialize
# --------------------------------------------------------------------------- #
FM_RE = re.compile(r"^---[ \t]*\n(.*?)\n---[ \t]*\n?(.*)$", re.DOTALL)


def split_front_matter(text: str):
    """Return (front_matter_dict, body_str). No front matter -> ({}, text)."""
    m = FM_RE.match(text)
    if not m:
        return OrderedDict(), text
    raw, body = m.group(1), m.group(2)
    return parse_yaml_subset(raw), body


def _scalar(token: str):
    token = token.strip()
    if token == "" or token in ("~", "null"):
        return None
    if (token[0] == '"' and token[-1] == '"') or (token[0] == "'" and token[-1] == "'"):
        return token[1:-1]
    if token in ("true", "True"):
        return True
    if token in ("false", "False"):
        return False
    if re.fullmatch(r"-?\d+", token):
        return int(token)
    return token


def _split_top_level(s: str):
    """Split on commas not nested inside quotes/brackets/braces."""
    out, depth, buf, quote = [], 0, [], None
    for ch in s:
        if quote:
            buf.append(ch)
            if ch == quote:
                quote = None
            continue
        if ch in ('"', "'"):
            quote = ch
            buf.append(ch)
        elif ch in "[{":
            depth += 1
            buf.append(ch)
        elif ch in "]}":
            depth -= 1
            buf.append(ch)
        elif ch == "," and depth == 0:
            out.append("".join(buf))
            buf = []
        else:
            buf.append(ch)
    if "".join(buf).strip():
        out.append("".join(buf))
    return out


def _parse_flow(value: str):
    value = value.strip()
    if value.startswith("[") and value.endswith("]"):
        inner = value[1:-1].strip()
        return [_scalar(p) for p in _split_top_level(inner)] if inner else []
    if value.startswith("{") and value.endswith("}"):
        inner = value[1:-1].strip()
        d = OrderedDict()
        for part in _split_top_level(inner):
            if ":" in part:
                k, v = part.split(":", 1)
                d[k.strip()] = _parse_flow(v) if v.strip()[:1] in "[{" else _scalar(v)
        return d
    return _scalar(value)


def parse_yaml_subset(raw: str) -> "OrderedDict":
    # Prefer PyYAML when present (handles any valid YAML the user hand-edits).
    try:
        import yaml  # type: ignore
        loaded = yaml.safe_load(raw)
        if isinstance(loaded, dict):
            return OrderedDict(loaded)
        if loaded is None:
            return OrderedDict()
    except Exception:
        pass

    data: "OrderedDict" = OrderedDict()
    lines = raw.split("\n")
    i = 0
    while i < len(lines):
        line = lines[i]
        if not line.strip() or line.lstrip().startswith("#"):
            i += 1
            continue
        if re.match(r"^\s", line):  # stray indented line with no parent key
            i += 1
            continue
        if ":" not in line:
            i += 1
            continue
        key, _, rest = line.partition(":")
        key = key.strip()
        rest = rest.strip()
        if rest:  # inline scalar / flow collection
            data[key] = _parse_flow(rest)
            i += 1
            continue
        # Empty value -> look ahead for an indented block (list or map).
        block, j = [], i + 1
        while j < len(lines) and (lines[j].startswith((" ", "\t")) or not lines[j].strip()):
            if lines[j].strip():
                block.append(lines[j])
            j += 1
        if block and block[0].strip().startswith("- "):
            data[key] = [_scalar(b.strip()[2:]) for b in block]
        elif block:
            sub = OrderedDict()
            for b in block:
                if ":" in b:
                    k, v = b.split(":", 1)
                    sub[k.strip()] = _parse_flow(v.strip()) if v.strip()[:1] in "[{" else _scalar(v)
            data[key] = sub
        else:
            data[key] = None
        i = j
    return data


def _needs_quote(s: str) -> bool:
    return (
        s == ""
        or s != s.strip()
        or any(c in s for c in ":#{}[],&*!|>'\"%@`")
        or s in ("true", "false", "null", "~")
        or bool(re.fullmatch(r"-?\d+", s))
    )


def _emit_scalar(v) -> str:
    if v is None:
        return "null"
    if isinstance(v, bool):
        return "true" if v else "false"
    if isinstance(v, (int, float)):
        return str(v)
    s = str(v)
    return '"' + s.replace('"', '\\"') + '"' if _needs_quote(s) else s


def _emit_value(v) -> str:
    if isinstance(v, list):
        return "[" + ", ".join(_emit_scalar(x) for x in v) + "]"
    if isinstance(v, dict):
        return "{" + ", ".join(f"{k}: {_emit_scalar(val)}" for k, val in v.items()) + "}"
    return _emit_scalar(v)


def serialize_front_matter(data: "OrderedDict") -> str:
    return "".join(f"{k}: {_emit_value(v)}\n" for k, v in data.items())


def compose(data: "OrderedDict", body: str) -> str:
    body = body if body.startswith("\n") or body == "" else "\n" + body
    if body and not body.endswith("\n"):
        body += "\n"
    return f"---\n{serialize_front_matter(data)}---\n{body}"


def read_file(path: str) -> str:
    if not os.path.isfile(path):
        raise PmError(f"no such file: {path}", code=3)
    with open(path, "r", encoding="utf-8") as fh:
        return fh.read()


# --------------------------------------------------------------------------- #
# Commands
# --------------------------------------------------------------------------- #
def cmd_init(args) -> int:
    repo = os.path.abspath(args.repo)
    template = os.path.abspath(args.template) if args.template else None
    os.makedirs(os.path.join(repo, ".pm-ops"), exist_ok=True)
    for rel in STAGE_DIRS.values():
        os.makedirs(os.path.join(repo, rel), exist_ok=True)

    created = []
    if template and os.path.isdir(template):
        for root, _dirs, files in os.walk(template):
            rel_root = os.path.relpath(root, template)
            dest_root = repo if rel_root == "." else os.path.join(repo, rel_root)
            os.makedirs(dest_root, exist_ok=True)
            for name in files:
                if name == ".gitkeep":
                    continue
                dest = os.path.join(dest_root, name)
                if not os.path.exists(dest):
                    with open(os.path.join(root, name), "r", encoding="utf-8") as src:
                        content = src.read()
                    with open(dest, "w", encoding="utf-8") as out:
                        out.write(content)
                    created.append(os.path.relpath(dest, repo))

    def ensure(rel, content):
        dest = os.path.join(repo, rel)
        if not os.path.exists(dest):
            with open(dest, "w", encoding="utf-8") as fh:
                fh.write(content)
            created.append(rel)

    cfg = dict(DEFAULT_CONFIG)
    if args.prefix:
        cfg["id_prefix"] = args.prefix
    ensure(".pm-ops/config.json", json.dumps(cfg, indent=2) + "\n")
    reg = dict(DEFAULT_REGISTRY)
    if args.prefix:
        reg["prefix"] = args.prefix
    ensure(".pm-ops/registry.json", json.dumps(reg, indent=2) + "\n")
    ensure("index.md", INDEX_HEADER + "\n_No items yet._\n")

    print(json.dumps({"repo": repo, "created": created, "ok": True}))
    return 0


def cmd_new_id(args) -> int:
    repo = find_repo_root(args.repo)
    reg_path = os.path.join(repo, ".pm-ops", "registry.json")
    if os.path.isfile(reg_path):
        with open(reg_path, "r", encoding="utf-8") as fh:
            reg = json.load(fh)
    else:
        reg = dict(DEFAULT_REGISTRY)
        os.makedirs(os.path.dirname(reg_path), exist_ok=True)
    prefix = args.prefix or reg.get("prefix") or "PM"
    nxt = int(reg.get("next", 1))
    reg["prefix"] = prefix
    reg["next"] = nxt + 1
    with open(reg_path, "w", encoding="utf-8") as fh:
        json.dump(reg, fh, indent=2)
        fh.write("\n")
    print(f"{prefix}-{nxt:04d}")
    return 0


def cmd_read(args) -> int:
    data, _body = split_front_matter(read_file(args.file))
    print(json.dumps(data, indent=2))
    return 0


def _coerce_value(raw: str):
    try:
        return json.loads(raw)
    except (ValueError, TypeError):
        return raw


def cmd_set(args) -> int:
    text = read_file(args.file)
    data, body = split_front_matter(text)
    if not args.assignments:
        raise PmError("set requires at least one KEY=VALUE")
    for pair in args.assignments:
        if "=" not in pair:
            raise PmError(f"bad assignment '{pair}' (expected KEY=VALUE)")
        key, _, raw = pair.partition("=")
        key = key.strip()
        if not re.fullmatch(r"[A-Za-z0-9_]+", key):
            raise PmError(f"invalid key '{key}'")
        data[key] = _coerce_value(raw)
    with open(args.file, "w", encoding="utf-8") as fh:
        fh.write(compose(data, body))
    print(json.dumps({"file": args.file, "set": [p.split("=", 1)[0] for p in args.assignments], "ok": True}))
    return 0


def cmd_normalize(args) -> int:
    data, _body = split_front_matter(read_file(args.file))
    task = OrderedDict()
    for key in NORMALIZED_KEYS:
        if key not in data:
            continue
        val = data[key]
        # Drop absent / empty values (incl. unfilled `{{...}}` template
        # placeholders, which parse to {} or "") so they never reach an engine.
        if val is None or val == "" or val == {} or val == []:
            continue
        task[key] = val
    if "id" not in task:
        raise PmError(f"{args.file} has no 'id' in front-matter; not a pm-ops artifact", code=2)
    for list_key in ("depends_on", "labels", "assignees"):
        if list_key in task and not isinstance(task[list_key], list):
            task[list_key] = [task[list_key]]
    print(json.dumps(task))
    return 0


def _iter_artifacts(repo: str):
    for stage, rel in STAGE_DIRS.items():
        d = os.path.join(repo, rel)
        if not os.path.isdir(d):
            continue
        for name in sorted(os.listdir(d)):
            if not name.endswith(".md"):
                continue
            path = os.path.join(d, name)
            try:
                data, _body = split_front_matter(read_file(path))
            except PmError:
                continue
            if data.get("id"):
                yield stage, path, data


def cmd_reindex(args) -> int:
    repo = find_repo_root(args.repo)
    rows = []
    for stage, path, data in _iter_artifacts(repo):
        board = data.get("board") or {}
        url = board.get("url") if isinstance(board, dict) else None
        ref = board.get("ref") if isinstance(board, dict) else None
        rows.append({
            "id": str(data.get("id", "")),
            "title": str(data.get("title", "")),
            "type": str(data.get("type", "")),
            "stage": stage,
            "status": str(data.get("status", "")),
            "rigor": str(data.get("rigor", "")),
            "spec": str(data.get("spec", "") or ""),
            "impl_repo": str(data.get("impl_repo", "") or ""),
            "board": f"[#{ref}]({url})" if url and ref else (str(ref) if ref else ""),
            "path": os.path.relpath(path, repo),
        })
    rows.sort(key=lambda r: r["id"])

    out = [INDEX_HEADER, f"\n**{len(rows)} item(s).**\n\n"]
    if rows:
        out.append("| ID | Title | Type | Stage | Status | Rigor | Board | Impl repo | File |\n")
        out.append("|----|-------|------|-------|--------|-------|-------|-----------|------|\n")
        for r in rows:
            title = r["title"].replace("|", "\\|")
            out.append(
                f"| {r['id']} | {title} | {r['type']} | {r['stage']} | {r['status']} | "
                f"{r['rigor']} | {r['board']} | {r['impl_repo']} | `{r['path']}` |\n"
            )
    else:
        out.append("_No items yet._\n")

    with open(os.path.join(repo, "index.md"), "w", encoding="utf-8") as fh:
        fh.write("".join(out))
    print(json.dumps({"repo": repo, "items": len(rows), "ok": True}))
    return 0


# --------------------------------------------------------------------------- #
# Arg parsing
# --------------------------------------------------------------------------- #
def build_parser():
    import argparse
    p = argparse.ArgumentParser(prog="pm.py", description="pm-ops deterministic core")
    sub = p.add_subparsers(dest="cmd", required=True)

    sp = sub.add_parser("init")
    sp.add_argument("--repo", default=".")
    sp.add_argument("--template", default=None)
    sp.add_argument("--prefix", default=None)
    sp.set_defaults(func=cmd_init)

    sp = sub.add_parser("new-id")
    sp.add_argument("--repo", default=".")
    sp.add_argument("--prefix", default=None)
    sp.set_defaults(func=cmd_new_id)

    sp = sub.add_parser("read")
    sp.add_argument("file")
    sp.set_defaults(func=cmd_read)

    sp = sub.add_parser("set")
    sp.add_argument("file")
    sp.add_argument("assignments", nargs="*")
    sp.set_defaults(func=cmd_set)

    sp = sub.add_parser("normalize")
    sp.add_argument("file")
    sp.set_defaults(func=cmd_normalize)

    sp = sub.add_parser("reindex")
    sp.add_argument("--repo", default=".")
    sp.set_defaults(func=cmd_reindex)
    return p


def main(argv=None) -> int:
    args = build_parser().parse_args(argv)
    try:
        return args.func(args)
    except PmError as e:
        sys.stderr.write(f"error: {e}\n")
        return e.code
    except Exception as e:  # noqa: BLE001
        sys.stderr.write(f"error: unexpected: {e}\n")
        return 1


if __name__ == "__main__":
    sys.exit(main())
